package com.example.lc4j.classification;

/**
 * ============================================================================
 * 情感分类枚举（分类任务的“类别集合”）
 * ============================================================================
 *
 * 【关键点】
 *   这只是一个普通的 Java 枚举。但当 AiService 接口的某个方法把它当作返回类型时，
 *   LangChain4j 会自动把下面这三个常量名拼进发给模型的提示词里，要求模型
 *   “只能在 POSITIVE / NEGATIVE / NEUTRAL 中选择一个”，然后把模型回答解析回枚举。
 *
 *   想新增类别？直接在这里加一个常量即可，提示词会自动带上它，无需改其它代码。
 * ============================================================================
 */
public enum Sentiment {
    /** 正面 / 好评 */
    POSITIVE,
    /** 负面 / 差评 */
    NEGATIVE,
    /** 中性 / 既非好评也非差评 */
    NEUTRAL
}
