package com.example.lc4j.mcp;

/**
 * ============================================================================
 * MCP 助手接口：一个“能调用远程 MCP 工具”的 AI Service
 * ============================================================================
 *
 * 【关键点】
 *   这仍然只是一个普通 Java 接口（和模块 04 一样由 AiServices 自动实现）。
 *   它本身并不知道远程有哪些工具——工具是运行时由 McpToolProvider 动态注入的。
 *   当你调用 chat(...) 时，模型若判断需要某个远程工具，就会自动发起调用，
 *   由 MCP 客户端转发给 MCP Server 执行，再把结果带回模型继续作答。
 * ============================================================================
 */
public interface McpAssistant {

    /**
     * 一句话进、一句话出。底层会把可用的远程 MCP 工具一并提供给模型，
     * 模型自行决定是否调用、调用哪个、传什么参数。
     */
    String chat(String userMessage);
}
