package com.example.lc4j.agents;

import dev.langchain4j.service.SystemMessage;

/**
 * ============================================================================
 * 研究助手智能体接口
 * ============================================================================
 *
 * 【关键点】
 *   - 仍是一个普通接口，由 AiServices 自动实现（模块 04）。
 *   - @SystemMessage 给它设定“人设 + 行为准则”：明确告诉它“可以多步使用工具”，
 *     这会引导模型在需要时主动调用 ResearchTools 里的工具。
 *   - 真正让它成为“智能体”的，是装配时挂上的 tools(工具) 和 chatMemory(记忆)（见 AgentRunner）。
 * ============================================================================
 */
public interface ResearchAgent {

    @SystemMessage("""
            你是一名严谨的研究助手。你可以使用提供的工具来获取数据和进行计算。
            完成任务的原则：
            1. 需要城市数据时，调用知识库查询工具，不要凭空编造数字。
            2. 需要做算术时，调用计算工具，不要自己心算。
            3. 你可以连续多步使用工具：先查资料、再计算、最后用中文给出清晰的结论。
            """)
    String research(String task);
}
