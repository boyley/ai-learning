package com.example.lc4j.structured;

/**
 * ============================================================================
 * 结构化输出的另一种形态：枚举（enum）
 * ============================================================================
 *
 * 当 AiService 接口方法返回类型是枚举时，LangChain4j 会把所有枚举常量
 * 作为“可选值”告诉模型，要求模型只能从中选一个返回。
 * 框架再把返回的字符串自动转成对应的枚举常量。
 *
 * 这是做“文本分类”最简单直接的方式（如情感分析）。
 * ============================================================================
 */
public enum Sentiment {
    /** 正面 / 积极 */
    POSITIVE,
    /** 负面 / 消极 */
    NEGATIVE,
    /** 中性 / 中立 */
    NEUTRAL
}
