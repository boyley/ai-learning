package com.example.lc4j.tools;

import dev.langchain4j.agent.tool.P;      // @P：描述某个工具方法的参数，帮助模型正确填参
import dev.langchain4j.agent.tool.Tool;   // @Tool：把一个普通方法暴露成“可被模型调用的工具”

/**
 * ============================================================================
 * 工具类：一组“可被大模型调用”的普通 Java 方法
 * ============================================================================
 *
 * 【关键点】
 *   - 这是一个最普通的类，方法里就是普通 Java 逻辑（可以查数据库、调接口、做计算……）。
 *   - 只要给方法加上 @Tool，并在装配 AiServices 时用 .tools(本对象) 注册进去，
 *     模型就能在对话中“看到”这些工具，并在需要时请求调用。
 *   - @Tool 的 value（即注解里写的字符串）= 工具描述，模型靠它判断“何时该用这个工具”。
 *   - @P 的 value = 参数描述，帮助模型把自然语言里的信息正确地填到参数上。
 *
 *   ★ 描述写得越清楚，模型调用得越准确——这就是“给工具写好说明书”。
 * ============================================================================
 */
public class AssistantTools {

    /**
     * 工具1：两数相加。
     * 模型遇到“3 加 5 等于几”这类问题时，会自动调用本方法而不是自己心算。
     */
    @Tool("计算两个整数相加的和")  // 工具描述：告诉模型这个工具能做什么
    public int add(@P("第一个加数") int a,
                   @P("第二个加数") int b) {
        System.out.println("  >> [工具被调用] add(" + a + ", " + b + ")");  // 打印以便观察模型确实调用了工具
        return a + b;
    }

    /**
     * 工具2：查询某城市天气（这里用写死的假数据模拟，真实项目会去调天气 API）。
     * 模型遇到“北京今天天气怎么样”时，会自动调用本方法获取数据再组织回答。
     */
    @Tool("查询指定城市的当前天气情况")  // 工具描述
    public String getWeather(@P("城市名称，例如：北京") String city) {
        System.out.println("  >> [工具被调用] getWeather(" + city + ")");  // 观察调用
        // 模拟数据：真实场景请替换为调用第三方天气服务
        return city + "：晴，气温 26℃，东南风 3 级";
    }
}
