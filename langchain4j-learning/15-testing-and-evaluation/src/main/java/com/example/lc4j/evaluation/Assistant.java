package com.example.lc4j.evaluation;

import dev.langchain4j.service.SystemMessage;

/**
 * ============================================================================
 * 被测助手接口（要被“裁判”打分的那个对象）
 * ============================================================================
 *
 * 【关键点】
 *   这就是一个普通的 AI Service 接口（见模块04）。
 *   它扮演“我们正在开发、需要评估其回答质量”的业务助手。
 *   稍后我们会把它的回答交给 Judge 去评分。
 * ============================================================================
 */
public interface Assistant {

    /**
     * 回答用户的问题。
     * @SystemMessage 给助手设定人设（这里故意要求“简洁、准确”，方便裁判按标准打分）。
     */
    @SystemMessage("你是一位严谨的科普助手，回答要准确、简洁，使用中文。")
    String answer(String question);
}
