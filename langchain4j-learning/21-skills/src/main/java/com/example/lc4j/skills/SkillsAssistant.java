package com.example.lc4j.skills;

import dev.langchain4j.service.SystemMessage;

/**
 * ============================================================================
 * 技能助手接口：一个「装配了 Skills 的」AI Service
 * ============================================================================
 *
 * 【关键点】
 *   这仍然只是一个普通 Java 接口（和模块 04 一样由 AiServices 自动实现）。
 *   它本身不含任何技能逻辑——技能是通过 AiServices 的 toolProvider 在运行时注入的。
 *   当你调用 chat(...) 时：
 *     1. 模型先看到各技能的「简短描述」；
 *     2. 若判断某技能相关，自动调用「激活技能」工具把该技能正文(content)拉进上下文；
 *     3. 再按技能里的规范/模板生成最终回答。
 * ============================================================================
 */
public interface SkillsAssistant {

    /**
     * 一句话进、一句话出。底层会把可激活的技能一并提供给模型，由模型自行决定是否激活。
     *
     * @SystemMessage 给模型一条系统指令。这里关键是要求模型激活技能时
     *   【逐字复制】available_skills 里列出的技能名——否则模型常会“意译”技能名
     *   （比如把「周报生成」说成「周报撰写」），导致激活失败。
     */
    @SystemMessage("""
            当你需要激活某个技能时，必须使用 <available_skills> 中列出的【确切技能名】，
            一字不差地原样复制该名称作为 activate_skill 的参数，不要翻译、改写或自行造名。
            """)
    String chat(String userMessage);
}
