package com.example.lc4j.rag;

import dev.langchain4j.service.SystemMessage;

/**
 * ============================================================================
 * AI Service 接口：一个“先查资料再回答”的知识库助手
 * ============================================================================
 *
 * 【关键点】
 *   还是一个普通接口（参考模块04），由 AiServices 动态实现。
 *   与模块04 的区别：装配时通过 .contentRetriever(...) 给它接上了一个“检索器”。
 *   于是每次调用 ask(...)，框架会【先去知识库检索相关片段，自动拼进提示词】，
 *   再交给模型作答——这套检索+拼接对调用方完全透明。
 * ============================================================================
 */
public interface Assistant {

    /**
     * 知识库问答。
     * 提示模型“依据检索到的资料回答”，能有效减少胡编（幻觉）。
     */
    @SystemMessage("你是公司知识库助手。请严格依据检索到的资料回答；若资料中没有，就如实说明你不知道。")
    String ask(String question);
}
