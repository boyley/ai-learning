package com.example.lc4j.tools;

import dev.langchain4j.service.SystemMessage;

/**
 * ============================================================================
 * AI Service 接口：一个“会用工具”的助手
 * ============================================================================
 *
 * 【关键点】
 *   这仍然只是一个普通 Java 接口（参考模块04），由 AiServices 动态实现。
 *   与模块04 唯一的不同是：装配时通过 .tools(...) 给它绑定了一组工具，
 *   于是这个助手在回答前可以“自己决定”要不要调用工具。
 *
 *   注意：接口本身【不需要】写任何工具相关的代码——工具是“外挂”进来的能力。
 * ============================================================================
 */
public interface Assistant {

    /**
     * 普通对话方法。
     * 运行时模型会看到我们注册的工具清单，按需自动调用，然后给出最终自然语言回答。
     */
    @SystemMessage("你是一个严谨的助手。涉及计算或天气时必须调用工具获取结果，不要自己编造。")
    String chat(String userMessage);
}
